      private void preencherTabela(){
        MontadoraDAO montadoradao = new MontadoraDAO();
        List<Montadora> montadoralista = montadoradao.getMontadora();
    
        DefaultTableModel tabelaMontadora = (DefaultTableModel) tblMontadora.getModel();
        
        //limpando a tabela para o filtro
        tabelaMontadora.setNumRows(0);
        
        for(Montadora m : montadoralista){
            Object[] obj = new Object[]{
                m.getId(),
                m.getNome(),
                m.getCnpj(),
                m.getTelefone(),
                m.getEndereco()
            };
        tabelaMontadora.addRow(obj);
        }
    
    }

