    private void preencherTabela(){
        CarroDAO carrodao = new CarroDAO();
        String filtrocarro = textFiltroCarro.getText();
        List<Carro> carroLista = carrodao.getCarro(filtrocarro);
    
        DefaultTableModel tabelaCarro = (DefaultTableModel) tblListaCarro.getModel();
        
        //limpando a tabela para o filtro
        tabelaCarro.setNumRows(0);
        
        for(Carro c : carroLista){
            Object[] obj = new Object[]{
                c.getId(),
                c.getMarca(),
                c.getModelo(),
                c.getCor(),
                c.getAno()            
            };
        tabelaCarro.addRow(obj);
        }
    
    }
    
